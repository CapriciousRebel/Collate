function isSymbol(characterCode) {
	if (characterCode >= 65 && characterCode <= 90) return false;
	if (characterCode >= 97 && characterCode <= 122) return false;
	if (characterCode >= 48 && characterCode <= 57) return false;
	if (characterCode == '_'.charCodeAt(0));
	return true;
}
function isWeird(characterCode) {
	if (characterCode >= 65 && characterCode <= 90) return false;
	if (characterCode >= 97 && characterCode <= 122) return false;
	if (characterCode >= 48 && characterCode <= 57) return false;
	return (acceptable.indexOf(String.fromCharCode(characterCode)) < 0);
}

function min(a, b) {
	return ((a < b) ? a : b);
}

function max(a, b) {
	return ((a > b) ? a : b);
}
function splitIntoWords(code, language) {
	// console.log(code);
	var listOfWords = [];
	var currentWord = "";
	for (i = 0; i < code.length; i++) {
		// console.log(currentWord + "                " + code[i]);
		// console.log("here");
		if (isWeird(code.charCodeAt(i)) || code[i] == ' ' || code[i] == '\n' || code[i] == '\t') {
			// console.log("here");
			if (currentWord != "") {
				listOfWords.push(currentWord);
				currentWord = "";
			}
			continue;
		}
		if (code[i] == '\"') {
			// console.log("here");
			++i;
			while (code[i] != '\"' && i < code.length) {
				++i;
			}
			--i;
			continue;
		}
		if (language == 2 && code[i] == '#') {
			currentWord = "";
			++i;
			while (code[i] != '\n' && i < code.length) {
				++i;
			}
			--i;
			continue;
		}
		if (isSymbol(code.charCodeAt(i))) {
			// console.log("here");
			if (currentWord == "" || isSymbol(currentWord.charCodeAt(currentWord.length - 1))) {
				currentWord += code[i];
			}
			else {
				listOfWords.push(currentWord);
				currentWord = "";
				currentWord += code[i];
			}
		}
		else {
			// console.log("here");
			if (currentWord == "" || !isSymbol(currentWord.charCodeAt(currentWord.length - 1))) {
				currentWord += code[i];
			}
			else {
				listOfWords.push(currentWord);
				currentWord = "";
				currentWord += code[i];
			}
		}
		if (language != 2) {
			if (currentWord == "//") {
				currentWord = "";
				++i;
				while (code[i] != '\n' && i < code.length) {
					++i;
				}
				--i;
				continue;
			}
			else if (currentWord == "/*") {
				currentWord = "";
				++i;
				var c = '*';
				while (i < code.length) {
					if (c + code[i] == "*/") break;
					else {
						c = code[i];
						i++;
					}
				}
			}
		}
	}
	if (currentWord != "") {
		listOfWords.push(currentWord);
	}
	return listOfWords;
}

function predict(wordList) {
	var howMany = min(100000, wordList.length);
	var prob = [];
	for (j = 0; j < 3; j++) {
		var temp = [];
		for (i = 0; i < howMany; i++) {
			temp.push(1.0);
		}
		prob.push(temp);
	}
	for (i = 0; i < howMany; i++) {
		if (wordList[i] in Data) {
			for (j = 0; j < 3; j++) {
				prob[j][i] = (Data[wordList[i]][j] + 1) / (wordCounts[j] + distinctWords);
			}
		}
		else {
			prob[0][i] = prob[1][i] = prob[2][i] = 1 / distinctWords;
		}
	}

	for (i = 0; i < 3; i++) {
		chances = [1.0, 1.0, 1.0];
		for (j = 0; j < 3; j++) {
			if (j == i) continue;
			for (k = 0; k < howMany; k++) {
				chances[j] = chances[j] * (prob[j][k] / prob[i][k]);
				chances[j] = min(chances[j], MMAX);
				chances[j] = max(chances[j], MMIN);
			}
		}
		var maxm = 0.0;
		var tot = 0.0;
		for (j = 0; j < 3; j++) {
			maxm = max(maxm, chances[j]);
			tot = tot + chances[j];
		}
		if (maxm == chances[i]) {
			for (j = 0; j < 3; j++) {
				chances[j] /= tot;
			}
			return chances;
		}
	}
	return [0.333333333, 0.333333333, 0.333333333];

}

function find(pred) {
	var index = -1;
	var maxm = 0.0;
	for (i = 0; i < 3; i++) {
		if (pred[i] > maxm) {
			maxm = pred[i];
			index = i;
		}
	}
	return index;
}


function Detect(code) {
	var languages = ["C++", "Java", "Python"];
	var wordsIfNotPython = splitIntoWords(code, 0);
	var wordsIfPython = splitIntoWords(code, 2);
	var prediction_1 = predict(wordsIfNotPython);
	var prediction_2 = predict(wordsIfPython);
	var index_1 = find(prediction_1);
	var index_2 = find(prediction_2);
	var detectedLanguage = "";
	if (index_1 == index_2) {
		detectedLanguage = languages[index_1];
	}
	else {
		if (prediction_2[index_2] > prediction_1[index_1] && index_2 == 2) {
			detectedLanguage = languages[index_2];
		}
		else {
			detectedLanguage = languages[index_1];
		}
	}
	return detectedLanguage;
}































