var acceptable = ['+', '-', '*', '/', '(', ')', '[', ']', '{', '}', '\'', '\"', '&', '^', '|', '#', '$', '!', '@', '%', ':', ';', '<', '>', ',', '.', '\\', '=', '_', '-', '~', '`'];
